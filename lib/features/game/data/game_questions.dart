class GameQuestion {
  final String question;
  final List<String> options;
  final int correctAnswerIndex;
  final String explanation;

  const GameQuestion({
    required this.question,
    required this.options,
    required this.correctAnswerIndex,
    required this.explanation,
  });
}

class ShuffledQuestion {
  final GameQuestion originalQuestion;
  final List<String> options;
  final int correctAnswerIndex;

  ShuffledQuestion._({
    required this.originalQuestion,
    required this.options,
    required this.correctAnswerIndex,
  });

  factory ShuffledQuestion(GameQuestion question) {
    final List<String> shuffledOptions = List<String>.from(question.options);
    final String correctAnswer = question.options[question.correctAnswerIndex];
    shuffledOptions.shuffle();
    final int newCorrectIndex = shuffledOptions.indexOf(correctAnswer);
    return ShuffledQuestion._(
      originalQuestion: question,
      options: shuffledOptions,
      correctAnswerIndex: newCorrectIndex,
    );
  }

  String get question => originalQuestion.question;
  String get explanation => originalQuestion.explanation;
}

